module.exports = async function handler(req, res) {
  if (req.method !== "POST") return res.status(405).json({ error: "Method not allowed" });

  const { message, history } = req.body;
  if (!message) return res.status(400).json({ error: "No message provided" });

  const conversationBlock = history ? history + "\n" : "";

  const systemPrompt = "You are Adrian Sousa, speaking in a direct, confident, and professionally grounded tone -- like you are in a real interview or a professional conversation with someone you respect. Speak in first person. You have earned these views. Do not hedge. Do not use filler. Use paragraphs for storytelling questions. Keep responses focused but never robotic. Never begin a response with casual or dismissive openers like Look, Honestly, or Here is the thing. Open with substance -- a direct, professional statement that earns the reader's attention. If asked about something not covered in your background, say: That is not something I have covered in my background, but feel free to ask about my experience or my work.";

  const fullPrompt = systemPrompt + "\n\nADRIAN BACKGROUND:\n" + RESUME_DATA + "\n\nCONVERSATION SO FAR:\n" + conversationBlock + "User: " + message.trim() + "\n\nAdrian:";

  try {
    const response = await fetch(
      "https://generativelanguage.googleapis.com/v1beta/models/gemini-3.1-flash-lite:generateContent?key=" + process.env.GEMINI_API_KEY,
      {
        method: "POST",
        headers: { "Content-Type": "application/json" },
        body: JSON.stringify({
          contents: [{ parts: [{ text: fullPrompt }] }],
          generationConfig: { temperature: 0.85, maxOutputTokens: 1024 }
        })
      }
    );

    const data = await response.json();
    const reply = data?.candidates?.[0]?.content?.parts?.[0]?.text || "No response generated.";
    res.status(200).json({ reply });
  } catch (err) {
    console.error("Chat API error:", err);
    res.status(500).json({ error: "Internal server error" });
  }
};
